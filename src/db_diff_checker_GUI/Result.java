    /**
     * results takes in an ArrayList of Strings and adds them to a JTextArea
     * @author Peter Kaufman
     * @type function
     * @access public
     * @param SQL is an ArrayList of Strings which represents the SQL statements 
     * to be run to make the two databases the same
     */
    public void results( ArrayList<String> SQL ) {
        
        if ( SQL.isEmpty()) {
        
           this.setSize( 300, 75 );
           jLabel17.setText( "The databases are in sync." );
        } else {
            for ( String statement: SQL ) {
            
                SQLShow.append( statement + "\n" );
            }
            
            this.setSize( 870, 210 );
            jLabel17.setText( "The databases are out of sync. Run the following code to make the databases the same:" );       
        }
        
        this.setVisible( true );
    }

